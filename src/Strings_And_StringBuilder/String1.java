package Strings_And_StringBuilder;

public class String1 {
    public static void main(String[] args) {
         
        // Strings are immutable means it cant not be changed after declared.
        String a  = "Tushar";
        System.out.println(a);

        a  = "kale";  // creating new object not changing str obj
        System.out.println(a);
    }
}
